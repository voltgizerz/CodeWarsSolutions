def decode(s):
    dict={'...': 's', '..-': 'u', '-.': 'n', '-..-': 'x', '.----': '1', '-': 't', '.--.': 'p', '.---': 'j', '....-': '4', '.-.': 'r', '.....': '5', '-----': '0', '-...': 'b', '-..': 'd', '----.': '9', '-....': '6', '--.-': 'q', '--..': 'z', '.--': 'w', '---': 'o', '..---': '2', '.-': 'a', '..': 'i', '-.-.': 'c', '...--': '3', '-.--': 'y', '....': 'h', '---..': '8', '...-': 'v', '--...': '7', '--.': 'g', '.': 'e', '--': 'm', '.-..': 'l', '..-.': 'f', '-.-': 'k'}
    s=s.split(" ")
    word =""
    for i in s:
       word+=str(dict.get(i," "))
    return word if word!=" " else ""